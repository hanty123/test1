void LibInitSens(int aiMode, int aiOption)
{	// PS6210K
	// aiMode   - 0 : Normal 30p, 1 : normal 60p
	// aiOption - 0 : OFF, 1 : DOL 2P

	UINT i;
	static BYTE bInitSet = 0x1;
	
	if(bInitSet){

		// Sensor H/W Reset
		GpioSetLo(GPIO_SRSTN);	WaitUs(10000);		GpioSetHi(GPIO_SRSTN);	WaitUs(10000);

		// I2C Check
		for(i=0; i<10; i++) {
		if(i2c_write(SENS_PXPLS_DEV,1,0) == 0) {
			gbSensorOff = 0;
			TxStrNoIRQ("SI2C connected.", 0, 0);
			break;
		}
		WaitUs(1000);
		}
		if(gbSensorOff) TxStrNoIRQ("SI2C not connected.", 0, 0);
		
		bInitSet = 0x0;
	}
	
	LibRdesIsp(aiMode, aiOption);

	//Initial Setting
	SetSens(0x03, 0x00);		//Bank A
	SetSens(0x05, 0x01);		// Mirror
	
//	for (i=0; i<gTblSensEa; i++)	{	SetSens(gwTblSens0[i][0], (BYTE)gwTblSens0[i][1+(aiMode*2)+((aiOption)?(1):(0))]);}
	for (i=0; i<gTblSensEa; i++)	{	SetSens(gwTblSens0[i][0], (BYTE)gwTblSens0[i][1]);}

	//Shutter Speed Max
	gnAeNorMax = gnInModeVtw-AE_SHTMAX_OFST;
	
}


