void LibRdesIsp(int aiResMode, int aiWdrMode)
{	
	UNUSED_ARG(aiResMode);
	
	// MIPI RX Configuration
	LibISPtoSENS_Intf(\
		/* MIPI or LVDS */\
		(MIPI),\
		/* 1, 2, 4 LANE */\
		(_2LANE),\
		/* 12BIT(Nor) or 10BIT(WDR) */\
		(_12BIT),\
		/* 3210, 10, 0 */\
		(_10),\
		/* Word Count */\
		(aiWdrMode == MN_OFF) ? (0xb4c) : (/*0x96a*/0xb4c),\
		/* MIPI Line information or Virtual channel mode */\
		(_MIPI_VC),\
		(aiWdrMode));
}

void LibInitSens(int aiMode, int aiOption)
{	// OS02K10, Sensor Master mode
	// aiMode   - 0 : 30p default -> Change HTS
	// aiOption - 0 : OFF, 1 : DOL 2P, 0x10 : DCG HDR(Single)
	
	UINT i;
	static BYTE bInitSet = 0x1;
	static BYTE bModeIndx = 0x0;

	// ISP - LVDS / MIPI Set
#if(0)
	TxStrNoIRQ("aiMode ", aiMode, 2);
	TxStrNoIRQ("aiOption ", aiOption, 2);
#endif

	if(bInitSet){
		// Sensor H/W Reset
		GpioSetLo(GPIO_SRSTN);	WaitUs(10000);		GpioSetHi(GPIO_SRSTN);

		for(i=0; i<10; i++) {
			if(i2c_write(SENS_OMNI_DEV,1,0) == 0) {
				gbSensorOff = 0;
				TxStrNoIRQ("SI2C connected.", 0, 0);
				break;
			}
			WaitUs(1000);
		}
		if(gbSensorOff) TxStrNoIRQ("SI2C not connected.", 0, 0);

		bInitSet = 0x0;
	}

	bModeIndx = ((aiMode==MD_CVBS)||(aiMode==MD_2M_FPS_50_60))?(1):(0);
	LibRdesIsp(bModeIndx, aiOption);

	// Linear
	SetSens(0x103, 0x1); // Sensor - S/W Reset
	WaitXms(10); // Delay 10ms

	// linear mode(Normal) : VTS - 8
	// Dual Exposure HDR, DCG_Exposure + VS_Exposure <= VTS - 10
	// 3-Exposure mode Long + Medium + Short <= VTS - 12
	if(aiOption == 0) // Linear Mode
	{
//		TxStrNoIRQ("Linear Mode : ", 1, 2);
		for (i=0; i<gTblSensEa; i++)	{SetSens(gwTblSens0[i][0], (BYTE)gwTblSens0[i][1+aiMode]);} // Normal Mode (30p)
	
		gbMode = 0;
	}
	else if(aiOption == 1) // Dual Exposure HDR
	{
//		TxStrNoIRQ("Dual Exposure HDR Mode : ", 1, 2);
		for (i=0; i<gLWDRTblSensEa; i++)	{SetSens(gwTblSens_LWDR[i][0], (BYTE)gwTblSens_LWDR[i][1]);} 	// HDR(Dual) Mode (30p)

		SetSens(0x3797, ((GetSens(0x3797)&(~ValSft_L07(0x1)))|ValSft_L07((0x1)&BitMask_01)));				// Fixed L to S Distance mode		

		// dp_delay_LS  = (0x37C9, 0x37CA)+1 : 0x12+1
		SetSens(0x37CA, 0x12); 

		// max_exposure_L <= VTS - 8 - max_exposure_S - max_exposure_VS		: 1125 - 8 - 17 = 1100
		// max_exposure_S <= dp_delay_LS - stg_adj[4:0]						: 0x13 - 2 = 0x11 = 17

		gnAeLWdrMaxL0	= 1100;
		gnAeLWdrMaxS1	= 17;

		gnAeLWdrMinL0	= 1;	//(AE_LWDR_2P_L0_MIN - AE_LWDR_2P_RHS1)-AE_LWDR_2P_L0MIN_OFST;
		gnAeLWdrMinS1	= 1;	//(AE_LWDR_2P_S1_MIN)-AE_LWDR_2P_S1MIN_OFST;

		gbMode = 0;
	}
	else if(aiOption == 0x10) // Single Exposure HDR
	{
//		TxStrNoIRQ("Single Exposure HDR Mode : ", aiMode, 2);

		if(bModeIndx==MD_2M_FPS_25_30)		for (i=0; i<gTblSens_CWDR_FHD30fpsEa; i++)	{SetSens(gwTblSens_CWDR_FHD30fps[i][0], (BYTE)gwTblSens_CWDR_FHD30fps[i][1]);} // Normal Mode
		else if(bModeIndx==MD_2M_FPS_50_60)	for (i=0; i<gTblSens_CWDR_FHD60fpsEa; i++)	{SetSens(gwTblSens_CWDR_FHD60fps[i][0], (BYTE)gwTblSens_CWDR_FHD60fps[i][1]);} // Normal Mode
	
		gbMode = 1;
	}
	
	WaitUs(1000); // delay for stabilize

	// Shutter Speed Max
	gnAeNorMax = gnInModeVtw-AE_SHTMAX_OFST;
}


